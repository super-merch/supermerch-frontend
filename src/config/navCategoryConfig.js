function resolveItem(item) {
  if (typeof item === "object" && item !== null) {
    return {
      label: item.label,
      backendName: item.backendName ?? item.label,
      categoryId: item.categoryId ?? null,
    };
  }
  return { label: item, backendName: item, categoryId: null };
}

export function buildMegaMenu(config, v1categories, handlers, parentType) {
  return config.map((category) => {
    const v1Cat = v1categories?.find((c) => c.id === category.id);
    const displayName = category.label || v1Cat?.name;

    return {
      id: category.id,
      name: displayName,
      onClick: () =>
        handlers.onCategory(
          v1Cat?.name || displayName,
          category.id,
          parentType,
        ),
      // Normalize items to plain label strings so NavigationMenu can render them
      columns: category.columns.map((col) => ({
        ...col,
        items: col.items.map((item) =>
          typeof item === "object" ? item.label : item,
        ),
      })),
      subItems: category.columns.flatMap((col) =>
        col.items.map((rawItem) => {
          const {
            label,
            backendName,
            categoryId: itemCatId,
          } = resolveItem(rawItem);

          // item → column → parent (most to least specific)
          const lookupId = itemCatId ?? col.categoryId ?? category.id;
          const lookupCat = v1categories?.find((c) => c.id === lookupId);

          const subType = lookupCat?.subTypes?.find(
            (st) => st.name.toLowerCase() === backendName.toLowerCase(),
          );

          const resolvedId =
            subType?.id ?? backendName.toLowerCase().replace(/\s+/g, "-");

          return {
            id: resolvedId,
            name: label,
            columnTitle: col.title,
            onClick: () =>
              handlers.onSubCategory(
                label,
                resolvedId,
                v1Cat?.name || displayName,
                parentType,
              ),
          };
        }),
      ),
    };
  });
}
